import java.util.Scanner;
class number
{
  public static void main(String[] args)
  {
    Scanner sc=new Scanner(System.in);
    long num=sc.nextLong();
    if(num>0)
      System.out.println("Positive");
    else if(num<0)
      System.out.println("Positive");
    else
      System.out.println("Zero");
  }
}
